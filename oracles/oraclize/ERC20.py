class Token:
    def __init__(self, address):
        web3 = Web3(HTTPProvider(WEB3_PROVIDER))
        self.erc20 = web3.eth.contract(address, abi=ABI)

    def balanceOf(self, address):
        return self.erc20.functions.balanceOf(address).call()
